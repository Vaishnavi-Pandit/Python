class Student:
    def setid(self, id):
        self.id = id

    def getid(self):
        return self.id

    def getname(self):
        return self.id
    def setname(self, name):
        self.name = name

    def getname(self):
        return self.name

    def setphone(self, ph):
        self.phone = ph

    def getphone(self):
        return self.phone

    def setadd(self, add):
        self.add = add

    def getadd(self):
        return self.add
    def setmarks(self,marks):
        self.marks=marks
    def getmarks(self):
        return self.marks
