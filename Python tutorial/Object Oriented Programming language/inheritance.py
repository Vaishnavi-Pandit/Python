# this is teacher class
class Teacher:
    def setid(self, id):
        self.Id = id

    def getid(self):
        return self.Id

    def setname(self, name):
        self.name = name

    def getname(self):
        return self.name

    def setphone(self, ph):
        self.phone = ph

    def getphone(self):
        return self.phone

    def setadd(self, add):
        self.add = add

    def getadd(self):
        return self.add
